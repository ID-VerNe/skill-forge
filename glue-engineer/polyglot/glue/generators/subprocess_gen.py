    def _bridge_extension(self, lang: str) -> str:
        """Get file extension for the foreign bridge."""
        return {
            "javascript": ".js",
            "rust": ".rs",
            "java": ".java",
            "kotlin": ".kt",
            "c_cpp": ".c",
        }.get(lang, ".txt")

    def _py_params(self, mapping) -> str:
        """Generate Python parameter list from a mapping."""
        params = []
        for pm in (mapping.param_mappings or []):
            p = self._ensure_pm(pm)
            if p.dst_name != "#MISSING":
                params.append(p.src_name)

        if not params:
            return "self"
        return "self, " + ", ".join(params)

    def _ensure_mapping(self, m):
        if isinstance(m, dict):
            return type("Obj", (), {
                "src_function": m.get("src_function", ""),
                "dst_function": m.get("dst_function", ""),
                "confidence": m.get("confidence", 0.0),
                "confidence_label": m.get("confidence_label", ""),
                "param_mappings": m.get("param_mappings", []),
                "error_map": m.get("error_map", {}),
            })()
        return m

    def _ensure_pm(self, p):
        if isinstance(p, dict):
            return type("Obj", (), {
                "src_name": p.get("src_name", ""),
                "dst_name": p.get("dst_name", ""),
                "src_default": p.get("src_default", ""),
                "dst_default": p.get("dst_default", ""),
            })()
        return p