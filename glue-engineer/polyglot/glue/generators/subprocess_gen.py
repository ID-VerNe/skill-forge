    def _generate_build_script(self, schema, dst_lang) -> str:
        """Generate build script for the foreign bridge."""
        lines = [
            f"#!/bin/bash",
            f"# Build script for {schema.pair_id} bridge",
            f"# Generated by glue-engineer v3",
            f"",
            f"set -e",
            f"",
            f"echo \"Building {schema.pair_id} bridge...\"",
            f"",
        ]
        if dst_lang == "rust":
            lines += [
                f"cd {schema.pair_id}/generated",
                f"# TODO: create proper Cargo.toml with {schema.dst.name if schema.dst else '?'} dependency",
                f'echo "Run: cargo build --release"',
                f"# cargo build --release",
                f"# cp target/release/bridge ../bridge",
            ]
        elif dst_lang == "javascript":
            lines += [
                f"cd {schema.pair_id}/generated",
                f"# TODO: ensure npm dependencies are installed",
                f"npm install {schema.dst.name if schema.dst else '?'}",
                f'echo "Bridge ready — run with: node bridge.js"',
            ]
        elif dst_lang in ("java", "kotlin"):
            lines += [
                f"cd {schema.pair_id}/generated",
                f"# TODO: compile bridge",
                f"# javac Bridge.java",
                f'echo "Bridge compiled"',
            ]
        elif dst_lang == "c_cpp":
            lines += [
                f"cd {schema.pair_id}/generated",
                f"# TODO: compile C bridge",
                f"# gcc -o bridge bridge.c -lcjson",
                f'echo "Bridge compiled"',
            ]
        else:
            lines += [
                f"# TODO: implement build for {dst_lang}",
                f'echo "Build script not implemented for {dst_lang}"',
            ]
        lines.append("")
        return "\n".join(lines)

